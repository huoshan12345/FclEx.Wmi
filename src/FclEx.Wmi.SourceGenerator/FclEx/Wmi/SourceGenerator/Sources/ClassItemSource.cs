using System;
using System.Linq;
using System.Management;
using System.Text;
using FclEx.CodeAnalysis;
using FclEx.Extensions;
using FclEx.Wmi.SourceGenerator.Models;

namespace FclEx.Wmi.SourceGenerator.Sources;

public static class ClassItemSource
{
    internal const string GeneratedFilesHeader = "// <auto-generated />";

    internal static SourceInfo Generate(string @namespace, IEnumerable<ClassItem> items, string group)
    {
        var usings = new List<string> { "System" };

        using var builder = new SourceBuilder()
            .WriteLine(GeneratedFilesHeader)
            .WriteLine()
            .WriteUsings(usings)
            .WriteLine();

        var ns = @namespace.SkipUntil("root\\", true, StringComparison.OrdinalIgnoreCase).Replace("\\", ".");

        // Namespace declaration
        builder.WriteLine($"namespace {ns}")
            .WriteOpeningBracket();

        foreach (var item in items.OrderBy(m => m.Name))
        {
            // Class description
            WriteQualifiers(builder, item.Qualifiers);

            var className = item.Name;
            // Class declaration
            builder.WriteLine($"public class {className}")
                .WriteOpeningBracket();

            foreach (var property in item.Properties.Where(m => m.CimType != CimType.Reference))
            {
                // Property description
                WriteQualifiers(builder, property.Qualifiers);

                var type = property.Type;
                if (property.IsArray)
                {
                    type += "[]";
                }
                builder.WriteLine($"public {type} {property.Name} {{ get; set; }}");
            }

            // End class declaration
            builder.WriteClosingBracket();
            builder.WriteLine();
        }

        // End namespace declaration
        builder.WriteClosingBracket();

        return ($"{ns}.{group}.g.cs", builder.ToString());
    }

    internal static void WriteQualifiers(SourceBuilder builder, Qualifiers qualifiers)
    {
        var lines = new List<string>();
        lines.AddRange(qualifiers.Descriptions);
        lines.Add(string.Empty);
        foreach (var (key, values) in qualifiers.Others)
        {
            var sb = new StringBuilder(256);
            sb.Append(key);
            sb.Append(": ");
            foreach (var (value, _, _, isLast) in values.IndexEx())
            {
                sb.Append(value);
                if (!isLast)
                {
                    sb.Append(", ");
                }
            }
            lines.Add(sb.ToString());
            lines.Add(string.Empty);
        }
        builder.WriteSummary(lines);
    }
}